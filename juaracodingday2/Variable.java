package juaracodingday2;

public class Variable {
    public static void main(String[] args) {
        int usia = 28, operasi = 10*5;
        float nilai = 8.7f;
        double nilaiProgramming = 8.9;
        System.out.println(usia);
        System.out.println(nilai);
        System.out.println(nilaiProgramming);
        System.out.println(operasi);

        //Char, Text
        char a = 'A';
        System.out.println(a);

        String nama = "Abdul Azis Fikri";
        System.out.println(nama);

        //Boolean
        boolean isAdrress = true;
        System.out.println(isAdrress);
    }
}
